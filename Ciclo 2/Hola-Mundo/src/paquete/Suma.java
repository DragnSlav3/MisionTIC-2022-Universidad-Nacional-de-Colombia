package paquete;

import java.util.*;


public class Suma {
    
public static void main(String[] args){
    
    int num1, num2, suma;
    
    Scanner input = new Scanner(System.in);
    
    System.out.print("Ingresa el primer número: ");
    
    num1 = input.nextInt();
    
    System.out.print("Ingresa el segundo número: ");
    
    num2 = input.nextInt();
    
    suma = num1 + num2;
    
    System.out.println("La suma es: " + suma);
    
    
    
}    
    
    

}
